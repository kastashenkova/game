package mainstage;

/**
 * Represents the possible directions a player can face or move in the game.
 */
public enum Direction {
  /**
   * Represents the upward direction.
   */
  up,
  /**
   * Represents the downward direction.
   */
  down,
  /**
   * Represents the leftward direction.
   */
  left,
  /**
   * Represents the rightward direction.
   */
  right,
  /**
   * Represents a default or neutral direction, often used when the player is stationary.
   */
  defaulted
}